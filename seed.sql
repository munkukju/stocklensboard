-- 4) 매매일지
CREATE TABLE trades (
  id          BIGINT AUTO_INCREMENT PRIMARY KEY,
  user_id     BIGINT NOT NULL,
  ticker      VARCHAR(10) NOT NULL,
  type        ENUM('BUY','SELL') NOT NULL,
  quantity    DECIMAL(15,4) NOT NULL,
  price       DECIMAL(12,2) NOT NULL,
  trade_date  DATE NOT NULL,
  note        VARCHAR(255),
  created_at  DATETIME DEFAULT CURRENT_TIMESTAMP,
  updated_at  DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  CONSTRAINT fk_trades_user   FOREIGN KEY (user_id) REFERENCES users(id)            ON DELETE CASCADE,
  CONSTRAINT fk_trades_ticker FOREIGN KEY (ticker)  REFERENCES stock_prices(ticker) ON DELETE RESTRICT,
  INDEX idx_trades_user_date (user_id, trade_date)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

